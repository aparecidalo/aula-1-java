console.log(c >= d); // Saída: true
// Comparando se c é menor ou igual a d
console.log(c <= d); // Saída: true
// Comparando se a é menor ou igual a c
console.log(a <= c); // Saída: false
// Comparando se a é maior ou igual a c
console.log(a >= c); // Saída: true